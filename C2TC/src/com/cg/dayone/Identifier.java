package com.cg.dayone;

public class Identifier {

	public static void main(String[] args) {
	
	  int a=10,b=20,temp=0;
	       
	       temp=a;             // a,b,temp are some of the identifiers used
	       a=b;
	       b=temp;
	       System.out.println("The swapped values are  " +a+" "+b);
	}

}
