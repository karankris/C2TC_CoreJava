package com.cg.day2.Control_Statements;
// print odd numbers using continue
public class Continue {
	              public static void main(String [] args)
	              {
	            	  int n=20;
	            	  for(int i=0;i<n;i++)	            		
	            	  {
	            		  if(i%2==0)
	            		  {
	            		  continue;
	            		  }
	            		  else
	            		  {
	            			  System.out.println(i);
	            		  }
	            			
	            	  }            	  
	              }
              	
}
