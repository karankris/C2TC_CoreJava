package com.cg.daytwo.looping;
public class For_each {
	public static void main(String[]args)
	{
	     int k[] = {10,20,30,40,50};
        for(int i:k)            // for each used only  array concepts
        {
        	System.out.println(i);// prints all the elements in the array
        }
  }
}
